#AND (para que devuelva true, los dos valores deben ser true)
resultado = True & True #devuelve true
resultado2 = False & True #devuelve false
resultado3 = True & True #devuelve false
resultado4 = False & False #devuelve false

#OR (solamente devuelve falso cuando ambas condiciones son false)
resultado5 = True | True
resultado6 = False | True
resultado7 = True | False
resultado8 = False | False

#NOT (invierte el valor)
resultado9 = not True (False)
resultado10 = not False (True)




